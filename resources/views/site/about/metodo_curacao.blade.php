                <h3 class="font-comfortaa text-main-2 text-center">
                    Rede Conceitual
                    <br>
                    <small>Conceitos de apoio ao aprendizado do Método Curação</small>
                </h3>
                <br><br>

                <h6 class="font-poppins text-main-1">
                    Acolhimento x Submetimento
                </h6>
                <p class="margin-top30 text-large">
                    Acolher é uma atitude de composição com os fatos, proporciona uma ampliação da alma daquele que
                    acolhe.
                    Submissão, ao contrário, é uma atitude de encolhimento, uma posição de contrariedade frente aos
                    fatos.
                    Acolhimento é o princípio básico do Método Curação.
                </p>

                <h6 class="font-poppins text-main-1 margin-top30">
                    Responsabilidade x Culpa
                </h6>
                <p class="margin-top30 text-large">
                    A responsabilidade e a culpa têm sido utilizadas como sinônimos. Entretanto, para nosso rigor
                    conceitual, são, na verdade, excludentes entre si. Vivemos em uma cultura que utiliza a culpa, em
                    vez da
                    responsabilidade, como base de seus princípios de educação e controle. Culpa é a primeira e mais
                    forte
                    criação de uma importância totalmente indevida que o sujeito inventa para si. Responsabilidade é
                    tomar
                    para si as escolhas e as ações para um viver saudável.
                </p>

                <h6 class="font-poppins text-main-1 margin-top30">
                    Fato x Trauma
                </h6>
                <p class="margin-top30 text-large">
                    O fato se refere ao reconhecimento da ocorrência de um evento, que poderá ser físico ou psíquico.
                    Ele
                    não traz em si um juízo de valor, não é bom nem ruim. Os fatos estão inscritos em um período de
                    tempo,
                    têm início e fim. O que fazemos a partir do fato são interpretações sobre eles. Essas interpretações
                    adquirem um colorido, de acordo aos valores de quem as significa. Algumas vezes, vivenciamos certos
                    fatos com muita contrariedade e, por não acolhê-los, nós os transformamos em traumas. O trauma é uma
                    construção emocional sobre o fato, significando-o de forma negativa. Nossa energia fica consumida no
                    reviver da situação e na busca de outro desfecho que jamais acontecerá, pois o passado não se
                    modifica.
                    Porém, é possível modificarmos nossas interpretações sobre fatos passados. Exatamente por isso,
                    somente
                    desconstruímos um trauma quando o acolhemos como um exigente e triste fato.
                </p>

                <h6 class="font-poppins text-main-1 margin-top30">
                    Dor x Sofrimento
                </h6>
                <p class="margin-top30 text-large">
                    A dor é inerente ao viver, enquanto que o sofrimento é opcional, já dizia Drummond. A dor, tanto
                    física
                    quanto emocional, é uma percepção que informa que algo ocorre na direção oposta do que gostaríamos.
                    Tem início e fim. Já o sofrimento é uma construção emocional que marca de forma exacerbada essa
                    contrariedade. Sofrimento é tudo o que reverbera e amplia a sensação de contrariedade aos fatos e à
                    realidade como ela se apresenta. Do acolhimento à dor, surge a tristeza, e da contrariedade a ela, o
                    sofrimento.
                </p>

                <h6 class="font-poppins text-main-1 margin-top30">
                    Desejar x Esperar
                </h6>
                <p class="margin-top30 text-large">
                    O desejo é a expressão de vontades conscientes ou inconscientes que dependem
                    da pessoa. Desejar demanda uma atitude ativa, na direção de agir para realizar
                    seus desejos. Esperar, de “esperança”, remete a uma atitude passiva, uma espera
                    que algo aconteça, independente de sua ação. Esperar é sinônimo de expectativa.
                    Ao esperarmos, nos desresponsabilizamos de nosso próprio cuidado.
                </p>

                <h6 class="font-poppins text-main-1 margin-top30">
                    Potência x Poder
                </h6>
                <p class="margin-top30 text-large">
                    A potência do sujeito é sua força de expansão, sua confiança na própria
                    capacidade de bem cuidar-se. Também a chamamos de potência criativa. A
                    potência pressupõe colaboração, cooperação, uma “potencialização” de
                    capacidades. Todos somos potentes e portadores de diferentes potências.
                    Diferente dela, o poder se relaciona a um delírio de controle. É uma força de
                    contração e opressão. O poder é uma ilusão, já que não somos poderosos e, sim,
                    podemos ocupar lugares de poder. O poder pressupõe submissão e superioridade.
                    A potência expande, o poder adoece.
                </p>

                <h6 class="font-poppins text-main-1 margin-top30">
                    Sentimento x emoção
                </h6>
                <p class="margin-top30 text-large">
                    A emoção é uma experiência afetiva que tem expressão no corpo, correspondendo a reações motoras e
                    metabólicas involuntárias. Os corpos manifestam, automaticamente, emoções sem a mediação da
                    consciência.
                    Em geral, as emoções se apresentam com padrões de expressão corporal características. Em função
                    disso,
                    podemos perceber quando alguém está alegre, com medo ou com raiva. O sentimento é o modo como a
                    pessoa
                    significa suas emoções; é subjetivo, da ordem do privado. É o resultado do processo consciente ou
                    inconsciente de significação. Em função disso, a construção do sentimento consciente de felicidade
                    requer contato do sujeito com sua alma e constante trabalho emocional.
                </p>

                <h6 class="font-poppins text-main-1 margin-top30">
                    Ações curativas X Ações impulsivas
                </h6>
                <p class="margin-top30 text-large">
                    As ações curativas são aquelas realizadas dentro do espaço de governabilidade de cada sujeito. São
                    mediadas pelas forças ativas, constituindo-se em ações saudáveis. As ações impulsivas são frutos de
                    descarga, e muitas vezes estão associadas à intenção da pessoa em evadir-se de sua ansiedade. São
                    mediadas pelas forças reativas e representam uma tentativa do sujeito de controlar o incontrolável.
                </p>

                <h6 class="font-poppins text-main-1 margin-top30">
                    Si próprio x si mesmo
                </h6>
                <p class="margin-top30 text-large">
                    O si-próprio vive no presente, sem aprisionar-se a fatos ou sentimentos passados, sem traumas, sem
                    ressentimentos. Existe um predomínio das forças ativas. Sua pauta é a ética consigo, com os valores
                    próprios e atuais em sua vida e não com a moral, que expressa valores coletivos externos, muitas
                    vezes sem qualquer correspondência com sua vida atual. O si-próprio acolhe os fatos e se acolhe
                    frente aos fatos, investigando seus afetos e escolhendo ações curativas a cada instante. O si-mesmo
                    vive embebido em críticas e culpas; aprisionado a um código de valores coletivo, idealizado,
                    carregado de juízos morais, de “certos e errados”. Correspondem, muitas vezes, aos valores
                    familiares e sociais, mas não aos valores atuais do sujeito, impedindo sua multiplicidade, seu
                    eu-caleidoscópio.
                </p>

